#include <stdio.h>

int main(void)
{
    int arr[10] = { 44, 32, 65, 23, 45, 76, 77, 89, 23, 45 };
    int* p; // 배열 원소를 가리킬 포인터
    int i;

    // 순방향 출력
    printf("배열: ");
    p = arr; // 배열의 첫 번째 원소를 가리키도록 설정
    for (i = 0; i < 10; i++) {
        printf("%d ", *(p + i)); // 포인터 연산으로 원소 접근
    }
    printf("\n");

    // 역방향 출력
    printf("역순: ");
    p = &arr[9]; // 배열의 마지막 원소를 가리킴
    for (i = 0; i < 10; i++) {
        printf("%d ", *(p - i)); // 포인터 연산으로 역순 접근
    }
    printf("\n");

    return 0;
}